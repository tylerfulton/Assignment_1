                <h1>Windy City Opry and Lethbridge Folk Club open new year this week</h1>
                <p class="date">Tuesday, 09 January 2018 </p>
                <h4>By Richard Amery</h4>
                <hr>
                <p>Things start to pick up this week with two major concert series starting the new year this week.<br><br>


 First up, The Windy City Opry returns to the Slice with a slate of performers including former  Lethbridgian Kory Istace, fellow Saskatoon based tour mate Aryn El Hefe and Red Deer‘s Boots and the hoots, whose founder Boots Graham started the Pinecone Opry in Red Deer and inspired Shaela Miller to start a similar series in the Windy City Opry. As always the evening begins at 8 p.m. sharp. There is a $10 cover charge.<br><br>


 The Lethbridge Folk Club rock in the new year with frenetic piano powered Calgary based rockabilly trio Peter and the Wolves, who play the Lethbridge College Cave, Saturday, Jan. 13. That show also begins at 8 p.m. sharp. <br><br>

The Folk Club also has their first open mic of the Year at Casa, Friday night at 7;30 p.m.<br>
 The Owl Acoustic Lounge has a busy week as usual. The Bryant Watson duo return, Friday, Jan. 12. The next night  things get loud and a little weird with Lethbridge’s MTBC and pop band  Open Channels plus Calgary rock and roll group the Night Committee.<br><br>

 Things will rock at the Slice, Jan 13 with local rock band Biloxi Parish , AJ Baragar’s band Rake Fight and Edmonton based, female fronted indie rock/alternative rock band King of Foxes.<br><br>
 
 If you are a little country and maybe a little rock and roll, Breanne Urban and Southern Flyer return to play two nights at Casino Lethbridge, Jan 12 and 13.<br><br>


 If you love “Hurtin’” country music, then local country trio Hurtin’ return to the Slice, Jan. 12 There is a $10 cover for the show.<br><br>


The first ukulele jam of the year happens at Casa, Sunday, Jan. 14. from 2-3 p.m. So bring you uke and come play along.<br><br>


Early next week, the Slice has a big rap show, Tuesday, Jan. 16, featuring Vancouver rapper Evil Ebenezer , plus East Coast O and J Marsh in addition to local talent the Psychonauts, Pyke, ONTK and JPB. Doors open at 8 p.m. Tickets are $10 in advance, $20 at the door.<br><br>

— By Richard Amery, L.A. Beat Editor</p>
